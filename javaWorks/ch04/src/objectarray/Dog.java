package objectarray;

public class Dog {
	
	private String dogName;
	private String type;
	
	// 기본 생성자
	public Dog() {}
	
	public void setDogName(String dogName) {
		this.dogName = dogName;
	}
	
	public String getDogName() {
		return dogName;
	}
	
	public void setType(String type) {
		this.type = type;
	}
	
	public String getType() {
		return type;
	}

}
